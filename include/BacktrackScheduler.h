#pragma once

#include "IScheduler.h"

namespace rrs {

// Алгоритм поиска с возвратом (backtracking) для составления расписания.
//
// Полный перебор по структуре:
//   тур_за_туром(r):
//     для каждого допустимого совершенного паросочетания M на оставшихся парах:
//       для каждого допустимого назначения хозяев в M:
//         попробовать M как тур r;
//         тур_за_туром(r+1);
//
// Используемые отсечения:
//   1. Доминирование: не строим пару (i,j), если уже сыграны оба направления.
//   2. Симметрия: фиксируем команду 0 в первом туре (первая пара содержит её).
//   3. Limit на breaks: при превышении лимита откатываемся.
//   4. Most-constrained-first: первой выбираем команду с минимальным числом партнёров.
//
// Цель — найти расписание с МИНИМАЛЬНЫМ числом breaks.
//
// Сложность: в худшем случае O((n-1)!! · R · n²) (без отсечений).
// На практике с отсечениями приемлемо для n ≤ 8; для n=10,12 уже долго,
// что является важным наблюдением для сравнительного анализа.

class BacktrackScheduler : public IScheduler {
public:
    BacktrackScheduler() = default;

    // time_limit_ms — мягкий лимит времени; при превышении возвращается
    // лучшее найденное к этому моменту расписание.
    explicit BacktrackScheduler(int time_limit_ms) : time_limit_ms_(time_limit_ms) {}

    [[nodiscard]] Schedule solve(int n_teams) override;
    [[nodiscard]] std::string name() const override { return "Backtracking"; }

private:
    int time_limit_ms_ = 5000;  // 5 секунд по умолчанию
};

} // namespace rrs
