#pragma once

#include "IScheduler.h"

namespace rrs {

// Алгоритм Бергера (Berger tables) — классический детерминированный
// конструктивный метод составления расписания кругового турнира.
//
// Принцип (для чётного n):
//   Команды {1,...,n}. Команда n фиксирована.
//   В туре r ∈ {1,...,n-1}:
//     - команда n играет с командой r;
//     - остальные команды разбиваются на пары:
//       (r+1, r-1), (r+2, r-2), ... — попарно симметрично от r,
//       индексы берутся mod (n-1), затем приводятся к диапазону {1,...,n-1}.
//
// Чередование дома/гости задаётся стандартным правилом Бергера:
//   - в нечётных турах команда с меньшим номером в паре играет дома, в чётных — наоборот.
//   - команда n играет дома/в гостях по чередующемуся образцу, чтобы избежать
//     серий одного типа.
//
// Второй круг получается зеркальным отражением первого с инверсией поля.
//
// Сложность: O(n²) по времени (n-1 туров × n/2 матчей).
//            O(n²) по памяти (хранение расписания).
class BergerScheduler : public IScheduler {
public:
    [[nodiscard]] Schedule solve(int n_teams) override;
    [[nodiscard]] std::string name() const override { return "Berger"; }
};

} // namespace rrs
